def old_almanac(user_id: int) -> str:
    # 用qq、日期生成随机种子
    random_seed_str = str([user_id, datetime.date.today()])
    md5 = hashlib.md5()
    md5.update(random_seed_str.encode('utf-8'))
    random_seed = md5.hexdigest()
    random.seed(random_seed)
    # 宜做和不宜做
    dd_do_and_not = random.sample(do, k=2)
    # 名句与DD
    dd_saying = random.sample(saying, k=1)
    dd_vtb = random.sample(vtb, k=1)

    result = f"【宜】\n{dd_do_and_not[0]['name']} —— {dd_do_and_not[0]['good']}\n\n" \
             f"【忌】\n{dd_do_and_not[1]['name']} —— {dd_do_and_not[1]['bad']}\n\n" \
             f"今日宜D：{dd_vtb[0]}\n\n“{dd_saying[0]}”"

    return result
